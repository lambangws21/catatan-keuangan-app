// lib/appwrite.ts
import { Client, Databases } from "appwrite";

const client = new Client()
  .setEndpoint(process.env.NEXT_PUBLIC_APPWRITE_ENDPOINT as string) // URL API Appwrite (contoh: http://localhost/v1 atau https://cloud.appwrite.io/v1)
  .setProject(process.env.NEXT_PUBLIC_APPWRITE_PROJECT as string);  // Project ID

export const databases = new Databases(client);
